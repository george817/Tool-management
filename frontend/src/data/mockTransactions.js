import { mockAssets } from './mockAssets'
import { mockUsers } from './mockUsers'

const actionTypes = ['issue', 'return', 'issue', 'maintenance', 'issue', 'return', 'overdue']
const day = 24 * 60 * 60 * 1000
const stamp = (daysAgo, minutesOffset = 0) => new Date(Date.now() - daysAgo * day - minutesOffset * 60 * 1000).toISOString()

export const mockTransactions = Array.from({ length: 144 }, (_, index) => {
  const asset = mockAssets[(index * 7) % mockAssets.length]
  const user = mockUsers[3 + ((index * 5) % (mockUsers.length - 3))]
  const action = actionTypes[index % actionTypes.length]

  return {
    id: index + 1,
    type: action,
    asset_id: asset.id,
    asset_name: asset.asset_name,
    asset_code: asset.asset_code,
    asset_type: asset.asset_type,
    user_id: user.id,
    user_name: user.name,
    department: user.department,
    issued_to: action === 'issue' ? user.name : asset.issued_to,
    condition: index % 11 === 0 ? 'fair' : 'good',
    remarks: action === 'maintenance' ? 'Scheduled service record synced.' : 'Demo transaction generated by mock service.',
    created_at: stamp(index % 60, index * 11),
  }
})

export const recordTransaction = (transaction) => {
  const next = {
    id: mockTransactions.length + 1,
    created_at: new Date().toISOString(),
    remarks: '',
    ...transaction,
  }
  mockTransactions.unshift(next)
  return next
}

